const DEFAULT_ICON = { lucide: "file-code" };

function renderIcon(iconDef) {
  if (iconDef.svg) {
    return (
      '<svg viewBox="0 0 24 24" fill="currentColor" aria-hidden="true">' +
      iconDef.svg +
      "</svg>"
    );
  }
  return '<i data-lucide="' + iconDef.lucide + '"></i>';
}

// Fix Zola shortcode body trimming: the first line of diagram content
// loses its leading whitespace. Detect and repair by comparing with line 2.
function fixDiagramIndent(codeEl) {
  var text = codeEl.textContent;
  var lines = text.split("\n");
  if (lines.length < 2) return;

  // Find first and second non-empty lines
  var first = -1, second = -1;
  for (var i = 0; i < lines.length; i++) {
    if (lines[i].trim().length > 0) {
      if (first === -1) first = i;
      else if (second === -1) { second = i; break; }
    }
  }
  if (first === -1 || second === -1) return;

  var firstIndent = lines[first].match(/^(\s*)/)[1].length;
  var secondIndent = lines[second].match(/^(\s*)/)[1].length;

  // If first line lost its indent (0 or much less than line 2), restore it
  if (firstIndent < secondIndent && firstIndent === 0) {
    lines[first] = " ".repeat(secondIndent) + lines[first];
    codeEl.textContent = lines.join("\n");
  }
}

document.addEventListener("DOMContentLoaded", function () {
  var codeBlocks = document.querySelectorAll(
    'pre[class*="language-"], pre.giallo, .highlight > .chroma',
  );

  codeBlocks.forEach(function (codeBlock) {
    var code = codeBlock.querySelector("code[data-lang]");
    if (!code) return;

    var lang = code.getAttribute("data-lang");
    if (!lang) return;

    // Fix diagram indentation before rendering labels
    if (lang === "diagram") {
      fixDiagramIndent(code);
    }

    if (codeBlock.querySelector(".code-lang-label")) return;

    var iconDef = LANG_ICONS[lang] || DEFAULT_ICON;
    var DISPLAY_NAMES = { shellscript: "Shell" };
    var displayName = code.getAttribute("data-title") || DISPLAY_NAMES[lang] || lang;

    var label = document.createElement("span");
    label.className = "code-lang-label";
    label.innerHTML = renderIcon(iconDef) + "<span>" + displayName + "</span>";

    codeBlock.appendChild(label);
  });

  if (window.lucide) {
    lucide.createIcons();
  }
});
